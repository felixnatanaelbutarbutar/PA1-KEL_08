                  <h4>Sejak 1977</h4>
                  <p>Gereja HKBP Peanajagar Tarutung adalah sebuah gereja Protestan yang terletak di Tarutung, Sumatra Utara, Indonesia.
                     Gereja ini adalah bagian dari Huria Kristen Batak Protestan (HKBP), salah satu denominasi terbesar di Indonesia. Gereja berikut merupakan tempat ibadah yang penting bagi jemaat mereka, serta menjadi pusat kegiatan pembinaan rohani, pendidikan agama, dan pelayanan sosial bagi masyarakat sekitar. 
                    Gereja HKBP Peanajagar Tarutung memiliki fitur-fitur artistik atau arsitektural yang unik, yang mencerminkan identitas dan nilai-nilai dari komunitas gereja tersebut.
                  </p>
                  <p>
                  Selain sebagai tempat ibadah, gereja juga dapat menjadi pusat kegiatan sosial dan keagamaan bagi komunitasnya.
                   Oleh karena itu, Gereja HKBP Peanajagar Tarutung mungkin juga menyediakan ruang untuk berbagai kegiatan sosial, pendidikan, dan keagamaan lainnya, seperti sekolah Minggu, kelas Alkitab, seminar, dan acara-acara lainnya.
                  </p>

